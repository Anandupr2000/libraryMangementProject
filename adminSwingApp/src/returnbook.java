    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        bookid = jTextField1.getText();
        studentid = jTextField2.getText();
        //String issuedate = jTextField3.getText();
        //String duedate = jTextField4.getText();
        try
        {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM issuedbook where bookid = '"+bookid+"' and studentid = '"+studentid+"'");
            if(rs.next())
            {
                jTextField3.setText(rs.getNString(3));
                jTextField4.setText(rs.getNString(4));
                jTextField1.setEditable(false);
                jTextField2.setEditable(false);
                jTextField3.setEditable(false);
                jTextField4.setEditable(false);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"NO Match Found");
                setVisible(false);
                new returnbook().setVisible(true);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

