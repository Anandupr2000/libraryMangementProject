    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try
        {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select returnbook from issuedbook where studentid = '"+studentid+"'");
            if(rs.equals("yes")||rs.equals("YES"))
            {
                Statement st1 = con.createStatement();
                st1.executeUpdate("update issuedbook set returnbook ='YES' where studentid = '"+studentid+"' and bookid = '"+bookid+"'");
                JOptionPane.showMessageDialog(null,"Book returned successfully");
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Student has already returned the book !");
            }
            setVisible(false);
            new returnbook().setVisible(true);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
            setVisible(false);
            new returnbook().setVisible(true);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

